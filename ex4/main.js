// khởi tạo biến chứa giá trị số thứ nhất khi ng dùng nhập
let fisrtNumber = Number(prompt("Nhập số thứ nhất:"));
// khởi tạo biến chứa giá trị số thứ hai khi ng dùng nhập
let secondNumber = parseFloat(prompt("Nhập số thứ hai:"));
// khởi tạo biến tổng của 2 số vừa nhập
let sum = fisrtNumber + secondNumber;
// hiển thị ra màn hình tổng của 2 số vừa nhập
alert("Tổng của " + fisrtNumber + " và " + secondNumber + " là: " + sum);
